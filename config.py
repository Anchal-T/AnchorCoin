# Block reward: the amount of new coin awarded to a miner for successfully mining a new block.
# This does not include transaction fees.
BLOCK_REWARD = 25.0

# Minimum transaction fee: the minimum fee required for a transaction to be considered for inclusion in a block.
# Transactions with fees lower than this may be ignored by miners.
MIN_TRANSACTION_FEE = 0.1
